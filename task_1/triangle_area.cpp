#include <cmath> // заголовочный файл с математическими функциями
/// Возвращает площадь равностороннего треугольника
/// a - сторона равностороннего треугольника
double calculate(double a){
	return sqrt(3)/4*a*a; // находим площадь равностороннего треугольника по формуле
}